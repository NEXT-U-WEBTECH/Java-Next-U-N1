/*
 La description : 
La méthode renvoie la forme minuscule de la valeur char spécifiée.

Syntax
boolean isLowerCase(char ch)

Paramètres :
ch − Type de caractère primitif.

Valeur de retour:
Cette méthode renvoie la forme minuscule de la valeur char spécifiée.
 */

public class Main {
    public static void main(String args[]) {
      System.out.println(Character.toLowerCase('c'));
      System.out.println(Character.toLowerCase('C'));
     }
}

/*
 * Cela produira le résultat suivant -

Production
c
c
 */
